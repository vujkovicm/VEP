selMostSev <- function(cons) {

	sev <- c("transcript_ablation", 
		 "splice_acceptor_variant", 
		 "splice_donor_variant", 
		 "stop_gained", 
		 "frameshift_variant", 
		 "stop_lost",
		 "start_lost",
		 "transcript_amplification",
		 "inframe_insertion",
		 "inframe_deletion", 
		 "missense_variant", 
		 "protein_altering_variant",
		 "splice_region_variant",
		 "incomplete_terminal_codon_variant", 
		 "stop_retained_variant",
		 "synonymous_variant",
		 "coding_sequence_variant",
		 "mature_miRNA_variant", 
		 "5_prime_UTR_variant", 
		 "3_prime_UTR_variant", 
		 "non_coding_transcript_exon_variant", 
		 "intron_variant",
		 "NMD_transcript_variant",
		 "non_coding_transcript_variant", 
		 "upstream_gene_variant", 
		 "downstream_gene_variant",
		 "TFBS_ablation", 
		 "TFBS_amplification", 
		 "TF_binding_site_variant",
		 "regulatory_region_ablation",
		 "regulatory_region_amplification",
		 "feature_elongation",
		 "regulatory_region_variant", 
		 "feature_truncation", 
		 "intergenic_variant")

	sev.up  <- toupper(sev)
	cons.up <- toupper(cons)
	sev.id  <- 1:length(sev.up)
	names(sev.id) <- sev.up

	if (grepl(",", cons.up, fixed=TRUE)) {
		cons.up.u <- unique(unlist(strsplit(cons.up, ",", fixed=TRUE)))
		return(sev.up[min(sev.id[cons.up.u])])
	} else {
		return(cons.up)
	}
}
